package metodos;

public class Carro 
{
	private String cor;
	private double preco;
	private String modelo;
	
	public Carro()
	{
		this.cor = "Branco";
		this.modelo = modelo;
		this.preco = preco;
	}
	public Carro(String modelo, double preco)
	{
		this.cor = "Branco";
		this.modelo = modelo;
		this.preco = preco;
	}
	public Carro(String cor, String modelo, double preco)
	{
		this.cor = "Branco";
		this.modelo = modelo;
		this.preco = preco;
	}
}
